// Send webhook request
async function sendWebhookRequest(taskData, retryCount = 0) {
  return new Promise((resolve, reject) => {
    const postData = JSON.stringify(taskData);
    
    const url = new URL(N8N_WEBHOOK_URL);
    const options = {
      hostname: url.hostname,
      port: url.port || (url.protocol === 'https:' ? 443 : 80),
      path: url.pathname,
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(postData),
        'User-Agent': 'Autonomous-TMS-24-7-Monitor/1.0',
        'X-Autonomous-Cycle': cycleCount.toString(),
        'X-Monitor-Version': '2.1.0'
      }
    };

    const protocol = url.protocol === 'https:' ? https : http;
    
    const req = protocol.request(options, (res) => {
      let data = '';
      res.on('data', (chunk) => {
        data += chunk;
      });
      
      res.on('end', () => {
        if (res.statusCode >= 200 && res.statusCode < 300) {
          successCount++;
          resolve({
            success: true,
            statusCode: res.statusCode,
            data: data,
            cycle: cycleCount
          });
        } else {
          errorCount++;
          reject(new Error(`HTTP ${res.statusCode}: ${data}`));
        }
      });
    });

    req.on('error', (error) => {
      errorCount++;
      reject(error);
    });

    req.write(postData);
    req.end();
  });
}

// Retry mechanism
async function sendWebhookWithRetry(taskData) {
  for (let attempt = 1; attempt <= MAX_RETRIES; attempt++) {
    try {
      const result = await sendWebhookRequest(taskData);
      return result;
    } catch (error) {
      console.log(`   âš ï¸  Attempt ${attempt}/${MAX_RETRIES} failed: ${error.message}`);
      
      if (attempt < MAX_RETRIES) {
        console.log(`   ğŸ”„ Retrying in ${RETRY_DELAY/1000} seconds...`);
        await new Promise(resolve => setTimeout(resolve, RETRY_DELAY));
      } else {
        throw error;
      }
    }
  }
}

